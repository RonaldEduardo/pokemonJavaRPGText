public class Pokemon {
  int tipo;
  String nome;
  int ataque;
  int defesa;
  int vida;


  public void inicializarPokemon(int tipo, String nome, int ataque, int defesa, int vida) {
    this.tipo = tipo;
    this.nome = nome;
    this.ataque = ataque;
    this.defesa = defesa;
    this.vida = vida;
  }

 
}
